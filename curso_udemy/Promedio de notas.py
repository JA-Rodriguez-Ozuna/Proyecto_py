#List
Student_grades = [90,89,76,79]

Mysum = sum(Student_grades)
length = len(Student_grades)
mean = Mysum / length
print(mean)

#Diccionary
Student_grades2 = {"Marry": 90, "Jose": 89, "Eneida": 76, "Jhon": 79}

Mysum = sum(Student_grades2.values())
length = len(Student_grades2.values())
mean = Mysum / length
print(mean)